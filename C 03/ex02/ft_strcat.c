char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	j;

	i = 0;
	while (dest[i] != '\0')
		i++;
	j = 0;
	while (src[j] != '\0')
	{
		dest[i + j] = src[j];
		j++;
	}
	dest[i + j] = '\0';
	return (dest);
}

/*Concatena strings, puede ser peligroso porque si la cadena a añadir es demasiado larga para 
contener las dos cadenas sobreescribirá la memoria adyacente. Por lo que puede causar 
comportamiento impredecibles*/